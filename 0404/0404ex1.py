print('한국', 'korea', 2023)
print('한국', 'korea', 2023, sep='-') # 구분자(-) 넣어서 출력
print('한국', 'korea', 2023, end='@') # 문장끝 @ 출력
print("\n") # 한 줄 띄우기
print('python', 'language'); print("Hi Python")
print('python', 'language', end=' '); print("Hi Python") # 줄바꿈 없이 출력
print("Hi python")
print("'Hi python'") # 작은 따옴표 출력
print("\'Hi python\'") # 작은 따옴표 출력
print("python " * 5) # 5번 반복출력
print("hi \n python") # \n(줄바꿈) 출력
print("\n 포맷 활용") # 한 줄 띄우고 문자열 출력




